Ejercicio 1:
Ingresar el tiempo de duración de una llamada telefónica y determinar la cantidad a pagar, de acuerdo con lo siguiente:
a. Toda llamada que dure tres minutos o menos tienen un costo de $300.
b. Cada minuto adicional cuesta $50.

Ejercicio 2:
Mostrar los numeros del 1 al 100, usando la instrucción for y una array indexado.

Ejercicio 3:
Mostrar las dos primeras letras de los dias de la semana, usando for y un array asociativo.

Ejercicio 4:
Dado un rango de números enteros obtener la cantidad de números pares que contiene.

Ejercicio 5:
Crear una función que permita calcular el cudrado, el cubo y la raiz cuadrada de un numero.